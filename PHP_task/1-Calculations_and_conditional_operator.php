    // 11 ---- Найдите число, полученное выписыванием в обратном порядке цифр данного трехзначного натурального числа.

    function reverseThreeNumeral($a) {
        echo "<p>Обратный порядок цифр числа $a : " . (($a % 10) * 100 + intdiv($a % 100, 10) * 10 + intdiv($a, 100)) . "</p>";   
    }
    
    //reverseThreeNumeral(123);


    // Доп ---- Найдите число, полученное выписыванием в обратном порядке цифр данного натурального числа.

    function reverseNumeral($a) {
        echo "<p>Обратный порядок цифр числа $a : ";
        $b = 0;
        while($a > 0) {
            $lastNum = $a % 10;
            $a = intdiv($a, 10);
            $b *= 10;
            $b += $lastNum;
        }
        echo "$b </p>";   
    }
    
