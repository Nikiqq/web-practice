    // 3 ---- Даны три числа. Найдите их среднее арифметическое.

    function average($a, $b, $c) {
        echo "<p>Среднее арифметическое чисел $a, $b и $c: " . ($a + $b + $c)/(func_num_args()) . "</p>";  
    }
    
